package com.celex.rider.DataModels;

public class TripModel {


    public String trip_distance;
    public String trip_duration;
    public String trip_request_distance;
    public String trip_date;
    public String trip_fixed_distance;




    public TripModel(String trip_distance, String trip_duration, String trip_request_distance, String trip_date, String trip_fixed_distance) {
        this.trip_distance = trip_distance;
        this.trip_duration = trip_duration;
        this.trip_request_distance = trip_request_distance;
        this.trip_date = trip_date;
        this.trip_fixed_distance = trip_fixed_distance;

    }

    public TripModel() {

    }






}
